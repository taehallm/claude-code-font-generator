export const generateAsciiArt = (text: string): string => {
  const inputLines = text.split('\n');
  const allAsciiLines: string[] = [];
  
  for (let lineIndex = 0; lineIndex < inputLines.length; lineIndex++) {
    const line = inputLines[lineIndex].toUpperCase();
    const lines = ['', '', '', '', '', ''];
    
    if (line.trim()) {
      for (let char of line) {
        if (ansiShadowFont[char]) {
          for (let i = 0; i < 6; i++) {
            lines[i] += ansiShadowFont[char][i];
          }
        } else {
          for (let i = 0; i < 6; i++) {
            lines[i] += '   ';
          }
        }
      }
      
      if (lineIndex > 0) {
        allAsciiLines.push('');
      }
      allAsciiLines.push(...lines);
    }
  }
  
  return allAsciiLines.join('\n');
};